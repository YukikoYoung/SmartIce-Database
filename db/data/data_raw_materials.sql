-- 4. 从临时表插入到正式产品表
-- ----------------------------------------------------------------------------
INSERT INTO product (
    product_code,
    product_name,
    product_type,
    category_id,
    is_saleable,
    is_ingredient,
    base_unit_id,
    storage_unit_id,
    current_cost_encrypted,
    cost_calculation_method,
    storage_condition,
    description,
    is_active
)
SELECT
    t.product_code,
    t.product_name,
    'raw_material',
    (SELECT category_id FROM product_category WHERE category_name =
        CASE t.category_name
            WHEN '肉类' THEN '肉类'
            WHEN '蔬菜' THEN '蔬菜类'
            WHEN '干杂' THEN '调料类'
            WHEN '海鲜' THEN '海鲜'
            WHEN '酱料' THEN '酱料'
            WHEN '酒水' THEN '酒水'
            WHEN '糖水铺' THEN '糖水铺'
            WHEN '自制' THEN '自制'
            WHEN '集采' THEN '集采'
            ELSE '调料类'
        END
        LIMIT 1
    ),
    FALSE,  -- 原材料默认不可销售
    TRUE,   -- 原材料可作为配料
    (SELECT unit_id FROM unit_of_measure WHERE unit_code = t.base_unit LIMIT 1),
    (SELECT unit_id FROM unit_of_measure WHERE unit_code = t.base_unit LIMIT 1),
    encrypt_cost(t.price_per_unit),
    'standard',
    t.storage_method,
    CONCAT('包装规格: ', COALESCE(t.package_spec, '-'),
           ' | 供应商: ', COALESCE(t.supplier_name, '-'),
           ' | 品牌: ', COALESCE(t.brand_name, '-'),
           ' | 备注: ', COALESCE(t.notes, '-')),
    TRUE
FROM temp_raw_materials t
ON CONFLICT (product_code) DO UPDATE SET
    product_name = EXCLUDED.product_name,
    current_cost_encrypted = EXCLUDED.current_cost_encrypted,
    storage_condition = EXCLUDED.storage_condition,
    description = EXCLUDED.description,
    updated_at = NOW();

-- 清理临时表
DROP TABLE IF EXISTS temp_raw_materials;

